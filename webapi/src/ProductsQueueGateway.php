public function deliveryDetails($user_id, $data)
{
    try {
        $deliverydetails = [];

        $sql = "
            SELECT 
                T1.prd_queue_code, 
                T1.orderedby, 
                T1.payee, 
                T1.pr_created_date, 
                T5.`desc`,
                T1.truck_details,
                T2.quantity,
                T2.cost_per_uom,
                T2.uom,
                T6.name
            FROM tbl_products_queue_summary AS T1
            LEFT JOIN tbl_products_queue AS T2 
                ON T1.prd_queue_code = T2.prd_queue_code
            LEFT JOIN lkp_busunits AS T3 
                ON T1.payee = T3.busunitcode
            LEFT JOIN lkp_supplier AS T4 
                ON T1.orderedby = T4.supplier_code
            LEFT JOIN lkp_build_of_products AS T5 
                ON T2.inv_code = T5.build_code
            LEFT JOIN lkp_busunits AS T6
                ON T1.orderedby = T6.busunitcode
            WHERE T1.truck_details != ''
              AND T1.payee = :busunitcode
              AND T1.pr_created_date BETWEEN :dateFrom AND :dateTo
            ORDER BY T1.seq ASC
        ";

        $stmt = $this->conn->prepare($sql);
        $stmt->bindValue(":busunitcode", $data["busunitcode"], PDO::PARAM_STR);
        $stmt->bindValue(":dateFrom", $data["dateFrom"] . " 00:00:00", PDO::PARAM_STR);
        $stmt->bindValue(":dateTo", $data["dateTo"] . " 23:59:59", PDO::PARAM_STR);
        $stmt->execute();


        while ($row = $stmt->fetch(PDO::FETCH_ASSOC)) {
            $deliverydetails[] = $row;
        }

        echo json_encode($deliverydetails);

    } catch (PDOException $e) {
        echo json_encode(["message" => "Error: " . $e->getMessage()]);
    }
}




